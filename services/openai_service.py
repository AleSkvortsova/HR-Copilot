def _chat_completion(system_prompt: str, user_content: str) -> str:
    if not OPENAI_API_KEY:
        raise ValueError("OPENAI_API_KEY не задан. Добавьте ключ в файл .env")

    client = OpenAI(api_key=OPENAI_API_KEY)
    response = client.chat.completions.create(
        model=OPENAI_MODEL,
        messages=[
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_content},
        ],
        temperature=OPENAI_TEMPERATURE,
        top_p=OPENAI_TOP_P,
        max_tokens=OPENAI_MAX_TOKENS,
    )

    return response.choices[0].message.content or "Не удалось получить ответ от модели."


